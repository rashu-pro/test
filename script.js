function findDuplicates(arr) {
    const duplicates = [];
    const uniqueValues = [];

    for (let i = 0; i < arr.length; i++) {
        const value = arr[i];

        if (uniqueValues.includes(value)) {
            if (!duplicates.includes(value)) {
                duplicates.push(value);
            }
        } else {
            uniqueValues.push(value);
        }
    }

    return duplicates;
}

